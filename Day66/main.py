@app.route("/random")
def get_random_cafe():
    result = db.session.execute(db.select(Cafe))
    all_cafes = result.scalars().all()
    random_cafe = random.choice(all_cafes)
    return jsonify()


with app.create_contect():
    db.create_all()


@app.route("/")
def home():
    return render_template("index.html")


